create or replace function public.audit_row_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  record_uuid text;
  mod_name text;
begin
  if tg_op = 'DELETE' then
    record_uuid := old.id::text;
    mod_name := coalesce(old.module_name, old.category, tg_table_name);
    insert into public.audit_logs(actor_id, action, table_name, record_id, module_name, old_data, new_data)
    values (auth.uid(), tg_op, tg_table_name, record_uuid, mod_name, to_jsonb(old), null);
    return old;
  else
    record_uuid := new.id::text;
    mod_name := coalesce(new.module_name, new.category, tg_table_name);
    insert into public.audit_logs(actor_id, action, table_name, record_id, module_name, old_data, new_data)
    values (auth.uid(), tg_op, tg_table_name, record_uuid, mod_name, case when tg_op='UPDATE' then to_jsonb(old) else null end, to_jsonb(new));
    return new;
  end if;
end
$$;

