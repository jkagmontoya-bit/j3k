create or replace function public.current_user_role()
returns text
language sql
security definer
set search_path = public
as $$
  select role::text from public.profiles where id = auth.uid()
$$;

create or replace function public.is_admin()
returns boolean
language sql
security definer
set search_path = public
as $$
  select coalesce((select role = 'admin' from public.profiles where id = auth.uid()), false)
$$;

create or replace function public.can_access_category(category_text text)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  r text;
  c text;
begin
  select public.current_user_role() into r;
  c := lower(coalesce(category_text, ''));

  if r = 'admin' then
    return true;
  end if;

  if r = 'contador' and c in ('compras', 'ventas', 'contabilidad', 'acuerdos', 'otros', 'bitacora', 'bitácora') then
    return true;
  end if;

  if r = 'rrhh' and c in ('rrhh', 'rh', 'recursos humanos') then
    return true;
  end if;

  if r = 'consulta' then
    return true;
  end if;

  return false;
end
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, display_name, role)
  values (new.id, new.email, coalesce(new.raw_user_meta_data->>'display_name', new.email), 'consulta')
  on conflict (id) do nothing;
  return new;
end
$$;

